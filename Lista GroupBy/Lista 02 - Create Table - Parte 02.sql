CREATE TABLE aluno (
	codigo BIGINT,
	nome VARCHAR(50),
	cidade varchar(50)
);

CREATE TABLE disciplina (
	sigla VARCHAR(3),
	nome VARCHAR(50)
);

CREATE TABLE professor (
	codigo BIGINT,
	nome VARCHAR(50),
	endereco VARCHAR(50),
	cidade VARCHAR(50)
);

CREATE TABLE turma (
	sigla_disciplina VARCHAR(3),
	codigo BIGINT,
	codigo_professor BIGINT,
	ano YEAR,
	periodo_inicio_fim VARCHAR(50)
);

CREATE TABLE historico (
	codigo BIGINT,
	codigo_aluno BIGINT,
	sigla_disciplina VARCHAR(3),
	n1 INTEGER,
	n2 INTEGER
);

